// What is SDLC ? Explain each phase of SDLC

/*ANS: SDLC means Software Development Life Cycle. SDLC is a systematic process used to design, develop, test, and deliver software products. It is a framework that outlines the stages involved in creating, maintaining, and replacing software applications.

1) Planning: Identifying the scope, purpose, and feasibility of the project. This includes resource allocation, cost estimation, and timeline setting.

2) Requirements Gathering and Analysis: Collecting detailed requirements from stakeholders to understand what the software must achieve. This often results in a requirements specification document.

3) Design: Creating the architecture of the software. This includes both high-level design, such as overall system architecture, and detailed design, such as database design and interface specifications.

4) Implementation (or Coding): Writing the actual code based on the design documents. This phase involves dividing the work into modules/units and developing them.

5) Testing: Verifying and validating that the software meets the required specifications. This includes various types of testing such as unit testing, integration testing, system testing, and acceptance testing.

6) Deployment: Releasing the software to a production environment where it can be used by the end-users. This may involve installation, configuration, and initial user training.

7) Maintenance: Providing ongoing support and modifications to the software after it is deployed. This includes bug fixing, performance improvements, and adapting the software to new requirements or environments.*/